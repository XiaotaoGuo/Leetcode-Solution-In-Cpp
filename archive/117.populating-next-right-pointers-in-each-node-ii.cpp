class Solution {
public:
    Node* connect(Node* root) {
        helper(root);
        return root;
    }

private:
    void helper(Node *root) {
        if (!root) return;
        
        Node *head=nullptr, *prev=nullptr;
        while(root){
            auto left = root->left, right = root->right;
            for (auto child: {left, right}){
                if (child){
                    if (!head) head = child;
                    if (prev) prev->next = child;
                    prev = child;
                }
            }
            root = root->next;
        }
        helper(head);
    }
};
